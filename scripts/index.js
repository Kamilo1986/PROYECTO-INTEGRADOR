function resaltarTexto(texto, termino) {
    if (!termino) return texto; 

    const regex = new RegExp(`(${termino})`, 'gi'); 
    return texto.replace(regex, '<span class="highlight">$1</span>'); 
}

function filtrarPorNombreYDescripcion(searchTerm) {
  searchTerm = searchTerm.toLowerCase().trim();
  return CardsData.filter(evento => 
      evento.Nombre.toLowerCase().includes(searchTerm) ||
      evento.Descripcion.toLowerCase().includes(searchTerm)
  );
}

function pintarTodasLasTarjetas(data) {
  const contenedor2 = document.getElementById('contenedor2');
  contenedor2.innerHTML = ''; 

  const searchTerm = document.getElementById('search-input').value.toLowerCase().trim();

  data.forEach(evento => {
      const divTarjeta = document.createElement('div');
      divTarjeta.classList.add('card');
      divTarjeta.innerHTML = `
          <div class="card-header">
              <img class="card-img-top" src="${evento.Imagen}" alt="${evento.Nombre}">
          </div>
          <div class="card-body">
              <h2 class="card-title badge text-bg-primary text-wrap d-flex justify-content-center align-items-center">
                  ${resaltarTexto(evento.Nombre, searchTerm)}
              </h2>
              <p class="card-text"><span>Fecha:</span> ${evento.Fecha}</p>
              <p class="card-text"><span>Categoria:</span> ${evento.Categoria}</p>
              <p class="card-text"><span>Descripcion:</span> ${resaltarTexto(evento.Descripcion, searchTerm)}</p>
              <p class="card-text"><span>Capacidad:</span> ${evento.Capacidad}</p>
              <p class="card-text"><span>lugar:</span> ${evento.lugar}</p>
              <p class="card-text"><span>Vendidos:</span> ${evento.Vendidos || 'undefined'}</p>
              <p class="card-text"><span>Asistentes:</span> ${evento.Asistentes || 'undefined'}</p>
              <h6 class="h6 d-flex align-items-center">$ ${evento.Precio}</h6>
          </div>
          <div class="detailsprice d-flex flex-row-reverse justify-content-around align-items-end">
              <a href="DETALLES.html?id=${evento._id}" class="btn btn-primary">Detalles</a>
            <button class="btn btn-success add-to-cart" data-id="${evento._id}" data-nombre="${evento.Nombre}" data-precio="${evento.Precio}">Agregar </button>
          </div>
      `;
      contenedor2.appendChild(divTarjeta);

      const detailsButton = divTarjeta.querySelector('.btn.btn-primary');
      detailsButton.addEventListener('click', () => {
          window.location.href = `DETALLES.html?id=${evento._id}`;
      });
  });

  if (data.length === 0) {
      contenedor2.innerHTML = '<p>No se encontraron eventos que coincidan con los criterios de búsqueda.</p>';
  }
}

function manejarBusquedaEnTiempoReal() {
    const searchTerm = document.getElementById('search-input').value;
    const eventosFiltrados = filtrarPorNombreYDescripcion(searchTerm);
    pintarTodasLasTarjetas(eventosFiltrados);
}

document.addEventListener('DOMContentLoaded', () => {
    const searchInput = document.getElementById('search-input');
    searchInput.addEventListener('input', manejarBusquedaEnTiempoReal);

    pintarTodasLasTarjetas(CardsData);
});

function filtrarPorCategoria() {
  const checkboxes = document.querySelectorAll('.form-check-input');
  const categoriasSeleccionadas = Array.from(checkboxes)
    .filter(checkbox => checkbox.checked)
    .map(checkbox => checkbox.value);

  if (categoriasSeleccionadas.length === 0) {
    pintarTodasLasTarjetas(CardsData);
  } else {
    const tarjetasFiltradas = CardsData.filter(evento =>
      categoriasSeleccionadas.includes(evento.Categoria)
    );
    pintarTodasLasTarjetas(tarjetasFiltradas);
  }
}

document.addEventListener('DOMContentLoaded', () => {
  const checkboxes = document.querySelectorAll('.form-check-input');
  checkboxes.forEach(checkbox => {
    checkbox.addEventListener('change', filtrarPorCategoria);
  });

  pintarTodasLasTarjetas(CardsData);
});

const cart = [];

function actualizarCarrito() {
  const cartItems = document.getElementById('cart-items');
  const totalPriceElement = document.getElementById('total-price');
  cartItems.innerHTML = '';
  let totalPrice = 0;

  cart.forEach(item => {
    const divItem = document.createElement('div');
    divItem.classList.add('cart-item');
    divItem.innerHTML = `
        <span>${item.Nombre}</span>
        <span>$${item.Precio}</span>
        <button class="remove-from-cart rounded-circle" data-id="${item._id}">Eliminar</button>
    `;
    cartItems.appendChild(divItem);
    totalPrice += item.Precio;
  });

  totalPriceElement.textContent = totalPrice.toFixed(2);

  document.querySelectorAll('.remove-from-cart').forEach(button => {
    button.addEventListener('click', eliminarDelCarrito);
  });
}

function agregarAlCarrito(event) {
  const id = event.target.getAttribute('data-id');
  const nombre = event.target.getAttribute('data-nombre');
  const precio = parseFloat(event.target.getAttribute('data-precio'));

  const item = { _id: id, Nombre: nombre, Precio: precio };
  cart.push(item);
  actualizarCarrito();
}

function eliminarDelCarrito(event) {
  const id = event.target.getAttribute('data-id');
  const index = cart.findIndex(item => item._id === id);
  if (index > -1) {
    cart.splice(index, 1);
    actualizarCarrito();
  }
}

document.addEventListener('DOMContentLoaded', () => {
  const addToCartButtons = document.querySelectorAll('.add-to-cart');
  addToCartButtons.forEach(button => {
    button.addEventListener('click', agregarAlCarrito);
  });

  document.getElementById('checkout-button').addEventListener('click', () => {
    alert('Gracias por su compra!');
    cart.length = 0; // Vaciar el carrito
    actualizarCarrito();
  });
});
